@Data
@NoArgsConstructor
@AllArgsConstructor
public class AppUser implements Serializable, UserDetails {

    private Integer userId;

    private String username;

    private String password;

    private String phone;

    private String userImage;

    private BigDecimal nowMoney;

    private Integer payCount;

    private String realName;

    private String cardId;

    private String mark;

    private String address;

    private String enable;

    private String effective;

    @JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss", timezone = "GMT+8")
    private Date createTime;

    @JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss", timezone = "GMT+8")
    private Date updateTime;

    private Collection<? extends GrantedAuthority> authorities;

    public AppUser(Integer userId, String username, String password, String enable, Collection<? extends GrantedAuthority> authorities) {
        this.userId = userId;
        this.username = username;
        this.password = password;
        this.enable = enable;
        this.authorities = authorities;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return this.authorities;
    }

    @Override
    public String getPassword() {
        return this.password;
    }

    @Override
    public String getUsername() {
        return this.username;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return StrUtil.equals(this.enable, "1");
    }
}
