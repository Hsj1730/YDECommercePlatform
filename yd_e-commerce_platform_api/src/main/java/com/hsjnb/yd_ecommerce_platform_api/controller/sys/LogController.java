@Api(tags = "系统管理 - 日志接口")
@RestController
@RequestMapping(value = "log")
public class LogController {

    private final LogService logService;

    @Autowired
    public LogController(LogService logService) {
        this.logService = logService;
    }

    /**
     * 获取用户操作日志
     * @return Result
     */
    @PostMapping(value = "getUserLogList", produces = Constant.CONTENT_TYPE)
    @ApiOperation(value = "获取用户操作日志", httpMethod = Constant.HTTP_POST, response = Result.class)
    public Result getUserLogList(@RequestBody Map<String,Object> param) {
        return Result.success(logService.getUserLogList(param));
    }

}
